    public Floricultura telaFloricultura(){
        String flor = (String)this.jFlor.getSelectedItem();
        String quantidade = (String)this.jQuantidade.getSelectedItem();
        String valor = this.jValor.getText();
        this.jValor.setText("");
        return new Floricultura(flor,valor,quantidade);
    }

    public void florTela(Floricultura f){
        this.jFlor.setSelectedItem(f.getFlor());
        this.jQuantidade.setSelectedItem(f.getQuantidade());
        this.jValor.setText(f.getValor());
    }
